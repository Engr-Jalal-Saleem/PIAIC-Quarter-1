"use strict";
/* The code is checking the value of the variable `num`. If `num` is greater than 0, it will print
"Positive". If `num` is less than 0, it will print "Negative". If `num` is equal to 0, it will print
"Zero". In this case, since `num` is 45, it will print "Positive". */
let num = 45;
if (num > 0) {
    console.log("Positive");
}
else if (num < 0) {
    console.log("Negative");
}
else {
    console.log("Zero");
}
